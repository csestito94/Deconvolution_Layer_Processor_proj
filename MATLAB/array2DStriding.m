%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%% 2D ARRAY STRIDING %%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
%   This function strides a 2D array by means of a S stride.
%   e.g. S = 2
%           [   a00     a01     a02    ]
%   A =     [   a10     a11     a12    ]
%           [   a20     a21     a22    ]
%
%
%           [   a00      0      a01      0      a02    ]
%           [    0       0       0       0       0     ]
%   O =     [   a10      0      a11      0      a12    ]
%           [    0       0       0       0       0     ]
%           [   a20      0      a21      0      a22    ]
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function strided_array = array2DStriding(array2D,S)
    IW = size(array2D,1);
    IH = size(array2D,2);
    OW = IW+(IW-1)*(S-1);
    OH = IH+(IH-1)*(S-1);
    strided_array(OW,OH) = zeros;
    for i = 1:IW
        for j = 1:IH
            strided_array((i-1)*S+1,(j-1)*S+1) = array2D(i,j);
        end
    end
end

 
    